public class Addition {
    
    /**
     * this adds two integers
     * @param a
     * @param b
     * @return 
     */
    public static int intergerSum(int a, int b){
        return a+b;
    }
    /**
     * this adds real numbers
     * @param a
     * @param b
     * @return 
     */
    public static double doubleSum(double a, double b){
        return a+b;
    }
}
